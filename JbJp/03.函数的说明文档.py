def syr_add(a, b):
    """
    :param a: 形参a表示其中的一个数字
    :param b: 形参b表示其中的另一个数字
    :return:  返回值是2数相加的结果
    """
    # param 用于解释参数
    # return用于解释返回值
    result = a + b
    print(f"{a} + {b} = {result}")
    return result


